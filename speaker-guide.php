			<h4>Guide for workshop instructors</h4>
			
			<p><em>TL;DR - It’s a workshop. Attendees should work.</em></p>
			
			<p>We are so grateful for your willingness to help Front be a success this year. We hope you know that the decision to invite you to lead a workshop was not a causal decision for us. We work very hard to find the best presenters who we feel confident will be able to provide a phenomenal learning experience for the people we do this for, our awesome attendees.</p>
			
			<p>While the Case Study Conference is hyper-focused on case study talks, our Workshop Series is meant to be the hands-on application of many of the important principles discussed at the conference. We are extremely hopeful that your workshop will include as much actual hands-on interaction and training as possible. Attendees will arrive expecting to work and act, not just listen.</p>
			
			<p>With that in mind, remember that that doesn’t just mean having them design or build things on their computer, under your watchful eye. It could also mean interacting as small groups, simulating or role-playing common situations, solving design or other sorts of problems in teams, sketching on paper, etc. We hope that you get creative. Four hours is a long time to hold and maintain attention, let alone fill professionals’ minds with new information. Don’t focus so much on quantity of information, but quality of adoption of knowledge, tools, and techniques.</p>
			
			
			
